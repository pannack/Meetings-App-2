import addingMeeting from "./add-meeting";

test("validateDate will validate Date", () => {
  const ap = new addingMeeting();
  document.body.innerHTML = `
<div class=" date-entry">
    <div class="label">
        <label for="date">Date</label>
    </div>
    <input type="date" id="date" class="date" /><br/><br/>
    <span class="message message-error"></span>
</div>
`;

ap.dateMeetEl = document.querySelector('.date') as HTMLInputElement;
const closest = (ap.dateMeetEl as HTMLElement).closest('.date-entry');
const message = closest?.querySelector('.message') as HTMLElement;
ap.dateMeetEl.value='16-10-2022';

const validateDate = ap.validateDate();
expect(validateDate).toEqual(true);
expect(message.innerHTML).toEqual('');
});

